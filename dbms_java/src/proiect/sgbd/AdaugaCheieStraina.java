        String Tabel1 = StangaCol.getSelectedItem().toString();
        String Tabel2 = DreaptaCol.getSelectedItem().toString();
        String col1 = StangaNume.getSelectedItem().toString();
        String col2 = DreaptaNume.getSelectedItem().toString();
        int p = DateTabele.verificareCheieStraina(Tabel1,Tabel2,col1,col2);
        System.out.println(p);
        if(p == 1)
        {
            int adaugaCheie = DateTabele.adaugaCheieStraina(Tabel1,Tabel2,col1,col2);
            if (adaugaCheie == 1 )
            {
                JOptionPane.showMessageDialog(null, "Referinta adaugata cu succes." , "Eroare", JOptionPane.INFORMATION_MESSAGE);
            }
            else 
            {
                JOptionPane.showMessageDialog(null, "Constrangere existenta." , "Eroare", JOptionPane.INFORMATION_MESSAGE);
            }
            
        }
        else if(p == -1)
        {
            JOptionPane.showMessageDialog(null, "Nu a fost gÄƒsit niciun Index unic pentru campul la care se face referire din tabelul principal. ", "Eroare", JOptionPane.INFORMATION_MESSAGE);
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Coloanele selectate nu au acelasi tip de date." , "Eroare", JOptionPane.INFORMATION_MESSAGE);
        }
               
